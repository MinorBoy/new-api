import { useCallback, useEffect, useRef } from 'react'

export interface CounterProps {
  end: number
  suffix?: string
  prefix?: string
  duration?: number
  decimals?: number
  className?: string
}

/**
 * Animated count-up span. Starts at 0, eases to `end` with a cubic
 * ease-out curve when the element scrolls into view (IntersectionObserver,
 * threshold 0.5), and only fires once per mount.
 *
 * Reduced motion: writes the final value immediately, no rAF loop.
 *
 * Extracted from features/home/components/sections/stats.tsx so the
 * Living System stats section (and any future caller) can reuse it
 * without duplicating the observer + animation plumbing.
 */
export function Counter(props: CounterProps) {
  const {
    end,
    suffix = '',
    prefix = '',
    duration = 1600,
    decimals = 0,
    className,
  } = props
  const ref = useRef<HTMLSpanElement>(null)
  const startedRef = useRef(false)

  const formatValue = useCallback(
    (v: number) =>
      decimals > 0 ? v.toFixed(decimals) : Math.round(v).toLocaleString(),
    [decimals]
  )

  const animate = useCallback(() => {
    const el = ref.current
    if (!el) return
    const start = performance.now()
    const step = (now: number) => {
      const progress = Math.min((now - start) / duration, 1)
      const eased = 1 - Math.pow(1 - progress, 3)
      el.textContent = `${prefix}${formatValue(eased * end)}${suffix}`
      if (progress < 1) requestAnimationFrame(step)
    }
    requestAnimationFrame(step)
  }, [end, duration, prefix, suffix, formatValue])

  useEffect(() => {
    const el = ref.current
    if (!el) return

    const mq = window.matchMedia('(prefers-reduced-motion: reduce)')
    if (mq.matches) {
      el.textContent = `${prefix}${formatValue(end)}${suffix}`
      return
    }

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting && !startedRef.current) {
          startedRef.current = true
          animate()
          observer.unobserve(el)
        }
      },
      { threshold: 0.5 }
    )

    observer.observe(el)
    return () => observer.disconnect()
  }, [animate, end, prefix, suffix, formatValue])

  return (
    <span ref={ref} className={className ?? 'tabular-nums'}>
      {prefix}0{suffix}
    </span>
  )
}
